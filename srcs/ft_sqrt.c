int		ft_sqrt(int nb)
{
	int		sqrt;

	sqrt = 0;
	if (nb < 1)
		return (0);
	while (sqrt <= nb / 2)
	{
		sqrt++;
		if (sqrt * sqrt == nb)
			return (sqrt);
	}
	return (0);
}
